IMPLICIT NONE
INTEGER ::   LMAX
REAL( KIND = LONG_REAL) :: EVEC_TOR(1:), EVEC_POL(1:)
TYPE ( HARMONIC_STRUCTURE ) :: HARMONICS(1:)

REAL( KIND = LONG_REAL), ALLOCATABLE :: COSTHETA_GRID(:), ALF(:,:), DALF(:,:), U_H(:)
REAL( KIND = EXTRA_LONG_REAL), ALLOCATABLE :: TEMP_THETA_TRANSFORM_ALF(:,:), TEMP_THETA_TRANSFORM_DALF(:,:)
REAL( KIND = LONG_REAL) :: PHI, PHI_DEP, DERV_PHI_DEP,  ONE_DIV_SIN_THETA, RANDOM_NUMBERS( 1:2 ), THETA
INTEGER :: I_THETA, I_PHI, I_HARMONIC, INDEX, i, I_LOC
INTEGER, ALLOCATABLE :: SEED(:)

! Generate LAT/LONG grid
CALL RANDOM_SEED(SIZE = i)
