SEED(:) = 1
CALL RANDOM_SEED(PUT = SEED)

ALLOCATE( COSTHETA_GRID(1))
OPEN(15, FILE = 'FLOW_VECTORS_RANDOM.DAT', STATUS = 'REPLACE', FORM = 'FORMATTED')

